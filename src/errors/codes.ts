export type ErrorCode = typeof ERROR_CODES[keyof typeof ERROR_CODES];

export const ERROR_CODE_NAMES = Object.fromEntries(
  Object.entries(ERROR_CODES).map(([key, value]) => [value, key])
);

export const getErrorName = (code: ErrorCode): string => {
  return ERROR_CODE_NAMES[code] || 'UNKNOWN_ERROR';
};